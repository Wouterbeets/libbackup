#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p;

	p = s1;
	while (*p != '\0')
	{
		p++;
	}
	while (*s2 != '\0' && n != 0)
	{
		*p = *s2;
		p++;
		s2++;
		n--;
	}
	*p = '\0';
	return (s1);
}
